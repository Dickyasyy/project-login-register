    private void tampil_data(){
        try{        
            String sql ="Select * From laborat";
            java.sql.Connection conn = (Connection)konfigurasi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            DefaultTableModel model = (DefaultTableModel)tabellaborat.getModel();
            model.setRowCount(0);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),
                    res.getString(4),res.getString(5),res.getString(6)});
                
            }          
            
        }catch(SQLException e){
            System.out.println("Error : "+e.getMessage());
        }
    }


    private void tabellaboratMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabellaboratMouseClicked
        // TODO add your handling code here:
        int baris = tabellaborat.rowAtPoint(evt.getPoint());

        String nama = tabellaborat.getValueAt(baris, 1).toString();
        txtnama.setText(nama);

        String nik = tabellaborat.getValueAt(baris, 2).toString();
        txtnik.setText(nik);

        String tanggal = tabellaborat.getValueAt(baris, 3).toString();
        txttanggal.setText(tanggal);

        String cek = tabellaborat.getValueAt(baris, 4).toString();
        cbpemeriksaan.setSelectedItem(cek);

        String harga = tabellaborat.getValueAt(baris, 5).toString();
        txtharga.setText(harga);
        
    }//GEN-LAST:event_tabellaboratMouseClicked


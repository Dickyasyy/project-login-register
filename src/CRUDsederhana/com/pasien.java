    private void cbsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbsimpanActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "INSERT INTO pasien (Nama,No_RM,NIK,No_telp,Alamat,Dirawat,Ruangan) values ('"+txtnama.getText()+"','"
                    +txtnorm.getText()+"','"
                    +txtnik.getText()+"','"
                    +txtnotelpon.getText()+"','"
                    +txtalamat.getText()+"','"
                    +cbdirawat.getSelectedItem()+"','"
                    +cbruangan.getSelectedItem()+"')";
            java.sql.Connection conn = (Connection)konfigurasi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Simpan Data Berhasil");
            tampilkan_data();
            kosongkan_form();

        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, "Simpan Data gagal");
        }
    }//GEN-LAST:event_cbsimpanActionPerformed


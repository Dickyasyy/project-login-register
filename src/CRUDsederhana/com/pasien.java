    private void tabelpengunjungMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpengunjungMouseClicked
        // TODO add your handling code here:
        int baris = tabelpengunjung.rowAtPoint(evt.getPoint());

        String nama = tabelpengunjung.getValueAt(baris, 1).toString();
        txtnama.setText(nama);

        String no_rm = tabelpengunjung.getValueAt(baris, 2).toString();
        txtnorm.setText(no_rm);

        String nik = tabelpengunjung.getValueAt(baris, 3).toString();
        txtnik.setText(nik);

        String no_telp = tabelpengunjung.getValueAt(baris, 4).toString();
        txtnotelpon.setText(no_telp);

        String alamat = tabelpengunjung.getValueAt(baris,5).toString();
        txtalamat.setText(alamat);

        String jumlah_dirawat = tabelpengunjung.getValueAt(baris, 6).toString();
        cbdirawat.setSelectedItem(jumlah_dirawat);

        String ruangan = tabelpengunjung.getValueAt(baris, 7).toString();
        cbruangan.setSelectedItem(ruangan);
    }//GEN-LAST:event_tabelpengunjungMouseClicked


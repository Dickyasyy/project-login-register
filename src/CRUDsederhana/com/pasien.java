    private void tampilkan_data(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nama");
        model.addColumn("No RM");
        model.addColumn("NIK");
        model.addColumn("No Telepon");
        model.addColumn("Alamat");
        model.addColumn("Dirawat");
        model.addColumn("Ruangan");
        
        String cari = txtcari.getText();
        try{         
            String sql ="Select * From pasien WHERE Nama LIKE'%"+cari+"%' OR No_RM LIKE'%"+cari+"%'" ;
            java.sql.Connection conn = (Connection)konfigurasi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),
                    res.getString(4),res.getString(5),res.getString(6),res.getString(7),res.getString(8)});
                
                tabelpengunjung.setModel(model);
            }          
            
        }catch(SQLException e){
            System.out.println("Error : "+e.getMessage());
        }
    }

